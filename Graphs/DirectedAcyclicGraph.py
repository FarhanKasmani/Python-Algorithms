# Every DP problem can be expressed in a Directed Acyclic Graph (DAG)
# Algorithms on DAG like
#     - Finding Shortest Path
#     - Finding Longest Path
#     - Counting number of paths to a given vertex
# give an optimal solution to DP problem
# 
# Some questions with more than one dependent constraints represent a graph.
# But by adding an extra condition converts that graph into DAG and above algorithms
# give solution to them.
